import random

def generate_random_numbers(count, start, end):
  return [random.randint(start, end) for _ in range(count)]

def main():
  print("Random Numbers:")
  numbers = generate_random_numbers(10, 1, 100)
  print(numbers)

if __name__ == "__main__":
  main()
